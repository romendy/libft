#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	char	*i;

	i = dest;
	while (*i != '\0')
		i++;
	while (*src != '\0')
	{
		*i++ = *src++;
	}
	*i = '\0';
	return (dest);
}
